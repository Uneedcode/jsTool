export function arrayToTree(
  array,
  id = "grpId",
  pid = "parentGrpId",
  children = "children"
) {
  const data = [...array];
  const result = [];
  const hash = {};
  data.forEach((item) => {
    hash[item[id]] = item;
  });
  data.forEach((item) => {
    // 判断一些有些奇葩数据就是 自己是根节点的时候自己的pId === id
    const hashVP = item[pid] === item[id] ? undefined : hash[item[pid]];
    if (hashVP) {
      if (!hashVP[children]) {
        hashVP[children] = [];
      }
      hashVP[children].push(item);
    } else {
      result.push(item);
    }
  });
  return result;
}
